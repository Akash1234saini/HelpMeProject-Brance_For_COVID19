package com.proapps.akashsaini.helpme;

public class Help {

    private int mImage;
    private String mTitle;

    public Help(int image, String title) {
        this.mImage = image;
        this.mTitle = title;
    }

    public int getImage() {
        return mImage;
    }

    public String getTitle() {
        return mTitle;
    }
}
